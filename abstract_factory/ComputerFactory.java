////////////////////////////////////////////////////////////////
package abstract_factory;
////////////////////////////////////////////////////////////////
interface ComputerFactory {
    CPU createCPU();
    Memory createMemory();
}
////////////////////////////////////////////////////////////////
class Acer implements ComputerFactory {
    public Intel createCPU() {
        return new Intel();
    }
    public CORSAIR createMemory() {
        return new CORSAIR();
    }
}
////////////////////////////////////////////////////////////////
class Lenovo implements ComputerFactory {
    public AMD createCPU() {
        return new AMD();
    }
    public Crucial createMemory() {
        return new Crucial();
    }
}
