abstract class AsciiArt {
    void brush( String patron ){
        System.out.print( patron );
    }
    abstract void hair();
    abstract void eyes();
    abstract void mouth();
    abstract void mbody();
    abstract void text();
    void draw(){
        hair();
        eyes();
        mouth();
        mbody();
        text();
        brush( "\n" );
    }
}
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
class DartVader extends AsciiArt {
    @Override
    public void hair() {
        brush( "C" );
    }
    @Override
    public void eyes() {
    }
    @Override
    public void mouth() {
        brush( "K" );
    }
    @Override
    public void mbody() {
        brush( ":;" );
    }
    @Override
    public void text() {
        brush( " Dart Vader" );
    }
}
////////////////////////////////////////////////////////////////
class Template {
    // CK:; Dart Vader
    public static void main( String args[] ){
        new DartVader().draw();
    }
}
