class SimpleFactoryPattern {
    public static void main( String args[]) {
        System.out.println( "Simple Factory" );
        var simpleFactory = new SimpleFactory();
        Animal animal = simpleFactory.create( "cat" );
        animal.speak();
        animal.action();
    }
}
////////////////////////////////////////////////////////////////
class SimpleFactory {
    Animal create(  String type ){
        if( type == "dog" ){
            return new Dog();
        }
        if( type == "cat" ){
            return new Cat();
        }
        throw new IllegalArgumentException();
    }
}
