package factory_method_pattern.jdp;
////////////////////////////////////////////////////////////////
interface Animal {
    void speak();
    void action();    
}
////////////////////////////////////////////////////////////////
class Dog implements Animal {
    public void speak() {
        System.out.println( "Woof" );
    }
    public void action() {
        System.out.println( "Playing" );
    }
}
////////////////////////////////////////////////////////////////
class Cat implements Animal {
    public void speak() {
        System.out.println( "Meou" );
    }
    public void action() {
        System.out.println( "Eating" );
    }
}
