////////////////////////////////////////////////////////////////
package decorator;
////////////////////////////////////////////////////////////////
abstract class Beverage {
    abstract void drink();
}
////////////////////////////////////////////////////////////////
class Coffee extends Beverage {
    @Override
    public void drink() {
        System.out.print( "Drinking coffee" );
    }
}
////////////////////////////////////////////////////////////////
class Espresso extends Beverage {
    @Override
    public void drink() {
        System.out.print( "Drinking espresso" );
    }
}
////////////////////////////////////////////////////////////////
